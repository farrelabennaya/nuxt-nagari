<template>
    <div data-hs-stepper='{"currentIndex": currentStep}'>
      <!-- Stepper Nav -->
      <ul class="relative flex flex-row gap-x-2">
        <li v-for="(step, index) in steps" :key="index" class="flex items-center gap-x-2 shrink basis-0 flex-1 group"
            :class="{'success': index < currentStep, 'active': index === currentStep}" data-hs-stepper-nav-item='{"index": index + 1}'>
          <span class="min-w-7 min-h-7 group inline-flex items-center text-xs align-middle">
            <span class="size-7 flex justify-center items-center shrink-0 bg-gray-100 font-medium text-gray-800 rounded-full group-focus:bg-gray-200 hs-stepper-active:bg-blue-600 hs-stepper-active:text-white hs-stepper-success:bg-blue-600 hs-stepper-success:text-white hs-stepper-completed:bg-teal-500 hs-stepper-completed:group-focus:bg-teal-600 dark:bg-neutral-700 dark:text-white dark:group-focus:bg-gray-600 dark:hs-stepper-active:bg-blue-500 dark:hs-stepper-success:bg-blue-500 dark:hs-stepper-completed:bg-teal-500 dark:hs-stepper-completed:group-focus:bg-teal-600">
              <span class="hs-stepper-success:hidden hs-stepper-completed:hidden">{{ index + 1 }}</span>
              <svg v-if="index < currentStep" class="hidden shrink-0 size-3 hs-stepper-success:block" xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="3" stroke-linecap="round" stroke-linejoin="round">
                <polyline points="20 6 9 17 4 12"></polyline>
              </svg>
            </span>
            <span class="ms-2 text-sm font-medium text-gray-800 dark:text-neutral-200">
              {{ step }}
            </span>
          </span>
          <div class="w-full h-px flex-1 bg-gray-200 group-last:hidden hs-stepper-success:bg-blue-600 hs-stepper-completed:bg-teal-600 dark:bg-neutral-700 dark:hs-stepper-success:bg-blue-600 dark:hs-stepper-completed:bg-teal-600"></div>
        </li>
      </ul>
      <!-- End Stepper Nav -->
    </div>
  </template>
  
  <script setup>
  import { computed } from "vue";
  import { useRoute } from "vue-router";
  
  const steps = ["Buat Keluarga", "Buat Kepala Keluarga", "Buat Alamat"];
  const route = useRoute();
  
  const currentStep = computed(() => {
    switch (route.name) {
      case "createKeluarga":
        return 1;
      case "createKepalaKeluarga":
        return 2;
      case "createAlamat":
        return 3;
      default:
        return 1;
    }
  });
  </script>
  
  <style scoped>
  /* Kustomisasi gaya stepper sesuai kebutuhan */
  </style>
  